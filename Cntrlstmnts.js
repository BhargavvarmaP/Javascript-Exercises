//Using do-while
var num=0;
do {
    console.log("Square of number "+num+" is : "+(num*num)+"\n");
    ++num;
}while(num<=20);
//Using While
p=0;
while(p<=10){
    console.log("Square of number "+p+" is : "+(p*p)+"\n");
    p=p+1;
}
//Using for 
for(q=0;q<=5;++q){
    console.log("Square of number "+q+" is : "+(q*q)+"\n");
}